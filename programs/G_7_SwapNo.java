// wajp to swap two no without using third variable
public class G_7_SwapNo {
	public static void main(String[] args) {
	
		int a,b,c;
		a=10;
		b=20;
		System.out.println("befor swaping:\n" + a + " " + b);
		c=a; 
		a=b;
		b=c;
		System.out.println("after swaping:\n" + a + " " + b);

	}

}
